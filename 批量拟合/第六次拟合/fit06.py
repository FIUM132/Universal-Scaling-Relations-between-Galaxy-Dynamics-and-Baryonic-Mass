HEADERS = ["星系名称", "总重子质量(M⊙)", "η((km/s)²)", "r0(kpc)", "alpha", "r1(kpc)", "beta", "拟合优度R²"]
CSV_FILE = "有效拟合星系结果.csv"

# ====================== 自动恢复数据 ======================
def auto_restore():
    if not os.path.exists(CSV_FILE) or os.path.getsize(CSV_FILE) < 100:
        print("🔄 自动恢复数据...")
        with open(CSV_FILE, "w", newline="", encoding="utf-8-sig") as f:
            w = csv.writer(f)
            w.writerow(HEADERS)
            w.writerows(ALL_GALAXIES)

# ====================== 终极清洗数据 ======================
def load_clean_data():
    M_b = []
    eta = []
    w = []
    with open(CSV_FILE, "r", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        for row in reader:
            try:
                mb = float(row["总重子质量(M⊙)"])
                et = float(row["η((km/s)²)"])
                r2 = float(row["拟合优度R²"])
                
                # 🔥 严格清洗：剔除所有垃圾点
                if (
                    r2 > 0.85 and          # 只留高质量拟合
                    et > 1000 and          # 剔除η=100的默认值
                    et < 2e5 and           # 剔除极端大的η
                    mb > 5e7 and           # 质量下限
                    mb < 2e11              # 质量上限
                ):
                    M_b.append(mb)
                    eta.append(et)
                    w.append(r2)
            except:
                continue
    return np.array(M_b), np.array(eta), np.array(w)

# ====================== 3σ 稳健剔除 ======================
def sigma_clip(x, y, w):
    def linear(x_, k, b):
        return k * x_ + b
    popt, _ = curve_fit(linear, x, y, sigma=1/(w+1e-6))
    y_pred = linear(x, *popt)
    res = np.abs(y - y_pred)
    med = np.median(res)
    mad = np.median(np.abs(res - med))
    mask = res < med + 3 * mad
    return x[mask], y[mask], w[mask]

# ====================== 主运行 ======================
if __name__ == "__main__":
    auto_restore()
    M_b, eta, weights = load_clean_data()
    print(f"✅ 清洗后样本数：{len(M_b)}")
    
    if len(M_b) < 3:
        print("❌ 样本不足")
        exit()
    
    # 对数变换
    logM = np.log10(M_b)
    logE = np.log10(eta)
    
    # 3σ剔除
    logM_c, logE_c, w_c = sigma_clip(logM, logE, weights)
    print(f"✅ 3σ剔除后样本数：{len(logM_c)}")
    
    # 高精度加权拟合
    def linear(x, k, b):
        return k * x + b
    popt, pcov = curve_fit(linear, logM_c, logE_c, sigma=1/(w_c+1e-6))
    k, b = popt
    dk, db = np.sqrt(np.diag(pcov))
    
    # R²
    y_pred = linear(logM_c, k, b)
    r2 = 1 - np.sum((logE_c - y_pred)**2) / np.sum((logE_c - np.mean(logE_c))**2)
    
    # 输出
    print("="*60)
    print("🎯 高精度最终结果")
    print("="*60)
    print(f"标度指数：k = {k:.4f} ± {dk:.4f}")
    print(f"拟合优度 R² = {r2:.4f}")
    print(f"标度关系：η ∝ M_b^({k:.3f}±{dk:.3f})")
